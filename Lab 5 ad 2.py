import math

# Obliczenia:
# Pierwiastek kwadratowy z 81
pierwiastek_z_81 = math.sqrt(81)
print("Pierwiastek kwadratowy z 81 wynosi:", pierwiastek_z_81)

# Pierwiastek kwadratowy z 810
pierwiastek_z_810 = math.sqrt(810)
print("Pierwiastek kwadratowy z 810 wynosi:", pierwiastek_z_810)

# Suma pierwiastków z 2, 3 i 6
suma_pierwiastkow = math.sqrt(2) + math.sqrt(3) + math.sqrt(6)
print("Suma pierwiastków z 2, 3 i 6 wynosi:", suma_pierwiastkow)

# Pierwiastek kwadratowy z 125
pierwiastek_z_125 = math.sqrt(125)
print("Pierwiastek kwadratowy z 125 wynosi:", pierwiastek_z_125)